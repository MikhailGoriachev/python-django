# Циклы - while, for


print('\n------------------------------------------------\n'
      'Цикл типа while')

a = 1                 # инициализация цикла
while a <= 5:         # заголовок - проверка условия продолжения
    print(a)          # тело цикла - полезная работа
    a += 1            # модификация, приводящая к выходу из цикла
else:                 # необязательная часть, выполняющаяся когда условие продолжения ложно
    print("a > 5, выход из цикла")

